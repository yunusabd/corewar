#include "libft.h"

int		ft_sqrt(int nb)
{
	int a;

	a = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	else
	{
		while (a <= nb / 2)
		{
			if (nb % a == 0)
			{
				if (nb / a == a)
					return (a);
			}
			a++;
		}
		return (0);
	}
}
