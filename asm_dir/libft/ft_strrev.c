#include "libft.h"

char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	k;

	i = 0;
	j = ft_strlen(str) - 1;
	k = str[0];
	while (j > i)
	{
		k = str[i];
		str[i] = str[j];
		str[j] = k;
		j--;
		i++;
	}
	return (str);
}
