#include "libft.h"

int	ft_putwstr(const wchar_t *s)
{
	int i;
	int ret;

	i = 0;
	ret = 0;
	while (s[i])
	{
		ret = ret + ft_putwchar(s[i]);
		i++;
	}
	return (ret);
}
