#include "vm.h"

void	move_pc(int *pc, int n)
{
	*pc += n;
	if (*pc > 0)
		*pc %= MEM_SIZE;
	else if (*pc < 0)
		*pc = (MEM_SIZE - *pc) % MEM_SIZE;
}
